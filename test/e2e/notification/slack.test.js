import loginDetails from '../setup/fixtures/login.json';
import slackNotification from '../setup/fixtures/notifications/slack.json';
import slackUpdatedNotification from '../setup/fixtures/notifications/update/slack.json';

describe('Notification - Slack', () => {
  context('create notification', () => {
    beforeEach(() => {
      const { email, password } = loginDetails.ownerUser;

      cy.clearCookies();
      cy.loginUser(email, password);
      cy.visit('/notifications');
    });

    it('should show invalid errors and create notification', () => {
      cy.createNotification(slackNotification);
    });
  });

  context('edit & delete notification', () => {
    beforeEach(() => {
      const { email, password } = loginDetails.ownerUser;

      cy.clearCookies();
      cy.loginUser(email, password);
      cy.visit('/notifications');
    });

    after(() => {
      cy.deleteNotification();
    });

    it('should show invalid errors and update notification', () => {
      cy.editNotification(slackUpdatedNotification);
    });
  });
});
